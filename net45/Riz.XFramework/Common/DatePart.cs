        /// 年
        /// </summary>
        Year = 1,

        /// <summary>
        /// 月
        /// </summary>
        Month = 2,

        /// <summary>
        /// 天
        /// </summary>
        Day = 3,

        /// <summary>
        /// 时
        /// </summary>
        Hour = 4,

        /// <summary>
        /// 分
        /// </summary>
        Minute = 5,

        /// <summary>
        /// 秒
        /// </summary>
        Second = 6,

        /// <summary>
        /// 毫秒
        /// </summary>
        Millisecond = 7,

        /// <summary>
        /// 计时周期（100纳秒）
        /// </summary>
        Tick = 8
    }
}
